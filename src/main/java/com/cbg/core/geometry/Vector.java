package com.cbg.core.geometry;

/**
 * @author Siebe
 *
 */
public class Vector {
    private float x;
    private float y;
    private float z;
    private float a;
    /**
     * @return the x
     */
    public float getX() {
        return x;
    }
    /**
     * @param x the x to set
     */
    public void setX(float x) {
        this.x = x;
    }
    /**
     * @return the y
     */
    public float getY() {
        return y;
    }
    /**
     * @param y the y to set
     */
    public void setY(float y) {
        this.y = y;
    }
    /**
     * @return the z
     */
    public float getZ() {
        return z;
    }
    /**
     * @param z the z to set
     */
    public void setZ(float z) {
        this.z = z;
    }
    /**
     * @return the a
     */
    public float getA() {
        return a;
    }
    /**
     * @param a the a to set
     */
    public void setA(float a) {
        this.a = a;
    }
}
